#include <stdio.h>

/**
 * add - adds two numbers
 * @a:integer
 * @b: integer
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a:integer
 * @b: integer
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a:integer
 * @b: integer
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a:integer
 * @b: integer
 * Return: division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - gives the remainder when a number is divided by another number
 * @a:integer
 * @b: integer
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
